let a = 5;
let b = 6;

console.log(a + b); // adicion / suma
console.log(a - b); // resta
console.log(a * b); // multiplicacion
console.log(a / b); // division
console.log(a % b); // modulo / lo que sobra d una division
console.log(a ** b); // potencia

/* Incrementar
El operador de incremento solo permite incrementar 1
*/
console.log(++a); // 5+1 = 6, y lo va a imprimir
console.log(a++); // 6+1 = 7, y lo va a imprimir
console.log(a);

/* Decrementar
El operador de decremento solo permite decrementar 1
*/
console.log(--a); // 7-1 = 6, y lo va a imprimir
console.log(a--); // 6-1 = 5, y lo va a imprimir
console.log(a);