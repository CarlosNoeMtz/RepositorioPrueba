// Operadores Relacionales
let a = 10;
 
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Operadores de Igualdad
console.log(a == 10);
console.log(a != 10);
/* El simbolo (!) significa 'Distinto'
La doble igualda solo compara el valor de la variable
*/ 
console.log(a == '10');
/*
Para comparar el valor y el tipo de dato 
*/
console.log(a === '10');
console.log(a !== '10');