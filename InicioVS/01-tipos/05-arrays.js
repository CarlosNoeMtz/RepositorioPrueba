/*
Los arrays son representados por parentesis cuadrados.
Los elementos de un array siempre comienzan en el indice '0'.
*/

let animales = ['perro','caballo','pajaro'];
console.log(animales);

// Para acceder a un elemento con un indice especifico:
console.log(animales[1]);

// Para agregar elementos al array:
animales[3] = 'gato';
console.log(animales);

// Para ver cuantos elementos contiene el array:
console.log(animales.length);

