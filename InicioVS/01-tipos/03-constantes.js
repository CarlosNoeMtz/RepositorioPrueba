/* 
Todas las variables que son declaradas con "let", pueden cambiar su valor en el futuro.
Solo debemos volver a declarar la variable, "No se recomienda hacer"
*/

let nombre = "Daniela";
nombre = "Alexa";

// Todas las palabras declaradas con "const", no podran cambiar su valor nunca
const apellido = "Martinez";

console.log(nombre);
console.log(apellido);